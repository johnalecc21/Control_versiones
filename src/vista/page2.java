    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String documento=txtdocumento.getText();
        String nombre=txtnombre.getText();
        String contrasena=txtcontrasena.getText();
        String rol= listarol.getSelectedItem().toString();
        String estado= listaestado.getSelectedItem().toString();
        if(documento.isEmpty()||contrasena.isEmpty()||nombre.isEmpty()){
            JOptionPane.showMessageDialog(this, "Favor de no dejar campos vacios");
        }else{
            ConexionDB co = new ConexionDB();
            int respuesta = co.Reusuario(documento,nombre, contrasena,rol,estado);
            if (respuesta==1) {
                JOptionPane.showMessageDialog(null, "Usuario registrado correctamente");
                txtdocumento.setText(null);
                txtnombre.setText(null);
                txtcontrasena.setText(null);
                MostrarTablaUsuario();
            }else{
                JOptionPane.showMessageDialog(null, "Error al registrar usuario");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        btnactualizar.setEnabled(false);
        jButton2.setEnabled(true);
        String documento=txtdocumento.getText();
        String nombre = txtnombre.getText();
        String contrasena =txtcontrasena.getText();
        String rol =(String) listarol.getSelectedItem();
        String estado =(String) listaestado.getSelectedItem();
        String idusuario = txtID.getText();
        if(documento.isEmpty()||contrasena.isEmpty()){
            JOptionPane.showMessageDialog(this, "Favor de no dejar campos vacios");
        }else{
            ConexionDB co=new ConexionDB();
            int respuesta=co.Acusuario(documento, nombre, contrasena, rol, estado, idusuario);

            if (respuesta==1) {
                JOptionPane.showMessageDialog(null, "Datos modificados");
                txtdocumento.setText(null);
                txtnombre.setText(null);
                txtcontrasena.setText(null);
                MostrarTablaUsuario();

            }else{
                JOptionPane.showMessageDialog(null, "Error al modificar");
            }   }
    }//GEN-LAST:event_btnactualizarActionPerformed

