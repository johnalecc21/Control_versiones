    private void tablausuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablausuariosMouseClicked
        // TODO add your handling code here:
        int clic_tabla =0;
        clic_tabla = this.tablausuarios.rowAtPoint(evt.getPoint());
        
        String idusuario = ""+tablausuarios.getValueAt(clic_tabla, 0);
        String documento = ""+tablausuarios.getValueAt(clic_tabla, 1);
        String nombre = ""+tablausuarios.getValueAt(clic_tabla, 2);
        String contrasena = ""+tablausuarios.getValueAt(clic_tabla, 3);
        String rol = ""+tablausuarios.getValueAt(clic_tabla, 4);
        String estado = ""+tablausuarios.getValueAt(clic_tabla, 5);
        
                
        int column = tablausuarios.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY()/tablausuarios.getRowHeight();
        
        if (row < tablausuarios.getRowCount() && row >=0 && column < tablausuarios.getColumnCount() && column >=0) {
            Object value = tablausuarios.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton)value).doClick();
                JButton boton = (JButton) value;
                
                if (boton.getName().equals("ed")) {
                    btnactualizar.setEnabled(true);
                    jButton2.setEnabled(false);
                    txtID.setText(idusuario);
                    txtdocumento.setText((documento));
                    txtnombre.setText(nombre);
                    txtcontrasena.setText(contrasena);
                    listarol.setSelectedItem(rol);
                    listaestado.setSelectedItem(estado);
        
                }
                 if (boton.getName().equals("el")) {
                   btnactualizar.setEnabled(false);
                   jButton2.setEnabled(true);
                   txtdocumento.setText(null);
                   txtnombre.setText(null);
                   txtcontrasena.setText(null);
                   ConexionDB co=new ConexionDB();
                   int respuesta=co.Elusuario(idusuario);
                   if (respuesta==1) {
                       JOptionPane.showMessageDialog(null, "Usuario eliminado correctamente");
                        MostrarTablaUsuario();

                   }else{
                       JOptionPane.showMessageDialog(null, "Error al eliminar usuario");
                   }           
                }
            }
        }
    }//GEN-LAST:event_tablausuariosMouseClicked

